package com.web.entity;

import java.io.Serializable;
/**
 * 微信回复消息设定实体类
 * @author Administrator
 *
 */
public class Reply implements Serializable {

	private int replyId;	//回复消息ID
	private String replyKeyWords;//用户输入关键字
	private int userWeiXinId;//外键关联微信用户记录表
	private UserWeiXin userWeiXin;
	
	public int getReplyId() {
		return replyId;
	}
	public void setReplyId(int replyId) {
		this.replyId = replyId;
	}
	public String getReplyKeyWords() {
		return replyKeyWords;
	}
	public void setReplyKeyWords(String replyKeyWords) {
		this.replyKeyWords = replyKeyWords;
	}
	public int getUserWeiXinId() {
		return userWeiXinId;
	}
	public void setUserWeiXinId(int userWeiXinId) {
		this.userWeiXinId = userWeiXinId;
	}
	public UserWeiXin getUserWeiXin() {
		return userWeiXin;
	}
	public void setUserWeiXin(UserWeiXin userWeiXin) {
		this.userWeiXin = userWeiXin;
	}
	
}

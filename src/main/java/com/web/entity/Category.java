package com.web.entity;

import java.io.Serializable;

public class Category implements Serializable {

	private int cateId;		//分类ID
	private String cateName;//分类名称
	private String cateImage;//分类图片
	private String cateUrl;	//分类外部连接地址，如果输入HTTP地址，就跳转外网，否则就读取该分类ID的文章信息
	private int cateSort;	//分类排序
	private int userWeiXinId;//外键，关联微信用户记录表
	private UserWeiXin userWeiXin;
	
	public int getCateId() {
		return cateId;
	}
	public void setCateId(int cateId) {
		this.cateId = cateId;
	}
	public String getCateName() {
		return cateName;
	}
	public void setCateName(String cateName) {
		this.cateName = cateName;
	}
	public String getCateImage() {
		return cateImage;
	}
	public void setCateImage(String cateImage) {
		this.cateImage = cateImage;
	}
	public String getCateUrl() {
		return cateUrl;
	}
	public void setCateUrl(String cateUrl) {
		this.cateUrl = cateUrl;
	}
	public int getCateSort() {
		return cateSort;
	}
	public void setCateSort(int cateSort) {
		this.cateSort = cateSort;
	}
	public int getUserWeiXinId() {
		return userWeiXinId;
	}
	public void setUserWeiXinId(int userWeiXinId) {
		this.userWeiXinId = userWeiXinId;
	}
	public UserWeiXin getUserWeiXin() {
		return userWeiXin;
	}
	public void setUserWeiXin(UserWeiXin userWeiXin) {
		this.userWeiXin = userWeiXin;
	}
	
}

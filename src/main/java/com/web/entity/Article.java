package com.web.entity;

import java.io.Serializable;

public class Article implements Serializable {

	private int articleId;		//文章管理主键
	private String title;		//文章标题
	private int cateId;	//外键，关联分类ID
	private Category category;
	private String articleImage;//封面图片
	private String articleContext;//图文详细内容
	private String articleUrl;	//外部连接地址
	private int userWeiXinId;//外键，关联微信用户记录表
	private UserWeiXin userWeiXin;

	public int getArticleId() {
		return articleId;
	}
	public void setArticleId(int articleId) {
		this.articleId = articleId;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public int getCateId() {
		return cateId;
	}
	public void setCateId(int cateId) {
		this.cateId = cateId;
	}
	public Category getCategory() {
		return category;
	}
	public void setCategory(Category category) {
		this.category = category;
	}
	public String getArticleImage() {
		return articleImage;
	}
	public void setArticleImage(String articleImage) {
		this.articleImage = articleImage;
	}
	public String getArticleContext() {
		return articleContext;
	}
	public void setArticleContext(String articleContext) {
		this.articleContext = articleContext;
	}
	public String getArticleUrl() {
		return articleUrl;
	}
	public void setArticleUrl(String articleUrl) {
		this.articleUrl = articleUrl;
	}
	public int getUserWeiXinId() {
		return userWeiXinId;
	}
	public void setUserWeiXinId(int userWeiXinId) {
		this.userWeiXinId = userWeiXinId;
	}
	public UserWeiXin getUserWeiXin() {
		return userWeiXin;
	}
	public void setUserWeiXin(UserWeiXin userWeiXin) {
		this.userWeiXin = userWeiXin;
	}
	
}
